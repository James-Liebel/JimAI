    desktop = Path.home() / "Desktop"
    output_dir = desktop
    try:
        output_dir.mkdir(parents=True, exist_ok=True)
    except Exception:
        output_dir = REPO_ROOT / "data" / "agent_space" / "runtime" / "desktop_shortcuts"
