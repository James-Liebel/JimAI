def load_pids() -> dict:
    if not PID_FILE.exists():
        return {}
    try:
        return json.loads(PID_FILE.read_text(encoding="utf-8"))
    except Exception:
        return {}


def save_pids(data: dict) -> None:
    ensure_runtime_dir()
    PID_FILE.write_text(json.dumps(data, indent=2), encoding="utf-8")


def is_running(pid: int) -> bool:
    try:
        os.kill(pid, 0)
    except OSError:
        return False
    return True


def terminate_pid(pid: int) -> bool:
    if not pid:
        return False
    try:
        os.kill(pid, signal.SIGTERM)
        return True
    except OSError:
        return False


def _wait_for_tcp_ready(
    name: str,
    host: str,
    port: int,
    timeout_seconds: float,
    proc: subprocess.Popen | None = None,
) -> tuple[bool, str]:
    probe_host = "127.0.0.1" if host in {"0.0.0.0", "::"} else host
    deadline = time.time() + timeout_seconds
    while time.time() < deadline:
        if proc is not None and proc.poll() is not None:
            return False, f"{name} exited early (code {proc.returncode})."
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        try:
            sock.settimeout(0.4)
            if sock.connect_ex((probe_host, int(port))) == 0:
                return True, ""
        except OSError:
            pass
        finally:
            sock.close()
        time.sleep(0.2)
    return False, f"{name} did not open tcp://{probe_host}:{port} within {timeout_seconds:.0f}s."


def choose_backend_python() -> str:
    venv_python = BACKEND_DIR / ".venv" / "Scripts" / "python.exe"
    if venv_python.exists():
        return str(venv_python)
    return sys.executable


def choose_tool(name: str) -> str:
    if os.name == "nt":
        local_bin = REPO_ROOT / "node_modules" / ".bin" / f"{name}.cmd"
        if local_bin.exists():
            return str(local_bin)
    if os.name == "nt":
        win = shutil.which(f"{name}.cmd")
        if win:
            return win
    resolved = shutil.which(name)
    if resolved:
        return resolved
    raise SystemExit(
        f"Required tool '{name}' was not found in PATH. "
        f"Install Node.js/npm and reopen the launcher."
    )


def _build_frontend_bundle(env: dict[str, str]) -> tuple[bool, str]:
    cmd = [choose_tool("npm"), "run", "build"]
    proc = subprocess.run(
        cmd,
        cwd=FRONTEND_DIR,
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        check=False,
    )
    if proc.returncode == 0:
        return True, ""
    tail = "\n".join((proc.stdout or "").splitlines()[-8:] + (proc.stderr or "").splitlines()[-8:]).strip()
    reason = tail if tail else f"npm build exited with code {proc.returncode}"
    return False, reason


def _start_frontend_static_server(
    args: argparse.Namespace,
    env: dict[str, str],
    creationflags: int,
) -> tuple[subprocess.Popen | None, str]:
    dist_dir = FRONTEND_DIR / "dist"
    if not dist_dir.exists():
        ok, reason = _build_frontend_bundle(env)
        if not ok:
            return None, f"static fallback build failed: {reason}"
    cmd = [
        choose_backend_python(),
        "-m",
        "http.server",
        str(args.frontend_port),
        "--bind",
        args.host,
    ]
    try:
        proc = subprocess.Popen(
            cmd,
            cwd=dist_dir,
            env=env,
            creationflags=creationflags,
        )
    except Exception as exc:
        return None, f"static fallback launch failed: {exc}"
    return proc, ""


