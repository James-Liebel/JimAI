def choose_n8n_command(args: argparse.Namespace) -> list[str]:
    custom = str(getattr(args, "n8n_command", "") or "").strip()
    if custom:
        return shlex.split(custom, posix=(os.name != "nt"))

    local_bin = (
        REPO_ROOT
        / "data"
        / "agent_space"
        / "tools"
        / "n8n"
        / "node_modules"
        / ".bin"
        / ("n8n.cmd" if os.name == "nt" else "n8n")
    )
    if local_bin.exists():
        return [str(local_bin), "start", "--port", str(args.n8n_port)]

    if os.name == "nt":
        global_n8n = shutil.which("n8n.cmd")
        if global_n8n:
            return [global_n8n, "start", "--port", str(args.n8n_port)]
        npx_bin = shutil.which("npx.cmd")
        if npx_bin:
            return [npx_bin, "n8n", "start", "--port", str(args.n8n_port)]
    else:
        global_n8n = shutil.which("n8n")
        if global_n8n:
            return [global_n8n, "start", "--port", str(args.n8n_port)]
        npx_bin = shutil.which("npx")
        if npx_bin:
            return [npx_bin, "n8n", "start", "--port", str(args.n8n_port)]

    raise SystemExit(
        "n8n executable was not found. Install n8n (npm install -g n8n) "
        "or run from the Automation tab with Install Local n8n."
    )


def start_services(args: argparse.Namespace) -> None:
    pids = load_pids()
    backend_pid = int(pids.get("backend_pid", 0) or 0)
    frontend_pid = int(pids.get("frontend_pid", 0) or 0)
    n8n_pid = int(pids.get("n8n_pid", 0) or 0)
    with_n8n = bool(getattr(args, "with_n8n", False))
    if backend_pid and is_running(backend_pid):
        print(f"Backend already running (PID {backend_pid}).")
    if frontend_pid and is_running(frontend_pid):
        print(f"Frontend already running (PID {frontend_pid}).")
    if with_n8n and n8n_pid and is_running(n8n_pid):
        print(f"n8n already running (PID {n8n_pid}).")
    if (
        backend_pid
        and is_running(backend_pid)
        and frontend_pid
        and is_running(frontend_pid)
        and (not with_n8n or (n8n_pid and is_running(n8n_pid)))
    ):
        if args.open:
            open_ui(args)
        return

    env = os.environ.copy()
    env["BACKEND_PORT"] = str(args.backend_port)
    env["FRONTEND_PORT"] = str(args.frontend_port)
    env["AGENT_SPACE_MODEL"] = args.model
    env["AGENT_SPACE_COMMAND_PROFILE"] = args.command_profile
    env["AGENT_SPACE_REVIEW_GATE"] = "true" if args.review_gate else "false"
    env["AGENT_SPACE_ALLOW_SHELL"] = "true" if args.allow_shell else "false"
    env["AGENT_SPACE_MAX_ACTIONS"] = str(args.max_actions)
    env["AGENT_SPACE_MAX_SECONDS"] = str(args.max_seconds)
    env["AGENT_SPACE_DESKTOP_MODE"] = "true" if args.desktop_mode else "false"
    env["AGENT_SPACE_DATA_ROOT"] = str(REPO_ROOT / "data" / "agent_space")
    env["AGENT_SPACE_GIT_CHECKPOINT"] = "true" if args.create_git_checkpoint else "false"
    env["AGENT_SPACE_PROACTIVE_ENABLED"] = "true" if args.proactive_enabled else "false"
    env["AGENT_SPACE_PROACTIVE_TICK_SECONDS"] = str(args.proactive_tick_seconds)
    env["AGENT_SPACE_N8N_ENABLED"] = "true"
    env["AGENT_SPACE_N8N_MODE"] = "managed" if with_n8n else "external"
    env["AGENT_SPACE_N8N_PORT"] = str(args.n8n_port)
    env["AGENT_SPACE_N8N_URL"] = args.n8n_url or f"http://localhost:{args.n8n_port}"
    env["AGENT_SPACE_N8N_AUTO_START"] = "true" if bool(getattr(args, "n8n_auto_start", False)) else "false"

    backend_cmd = [
        choose_backend_python(),
        "-m",
        "uvicorn",
        "main:app",
        "--host",
        args.host,
        "--port",
        str(args.backend_port),
    ]
    frontend_cmd = [
        choose_tool("npm"),
        "run",
        "dev",
        "--",
        "--host",
        args.host,
        "--port",
        str(args.frontend_port),
    ]

    creationflags = 0
    if os.name == "nt":
        creationflags = subprocess.CREATE_NEW_PROCESS_GROUP  # type: ignore[attr-defined]

    backend_proc = subprocess.Popen(
        backend_cmd,
        cwd=BACKEND_DIR,
        env=env,
        creationflags=creationflags,
    )
    try:
        time.sleep(1)
        frontend_proc = subprocess.Popen(
            frontend_cmd,
            cwd=FRONTEND_DIR,
            env=env,
            creationflags=creationflags,
        )
    except Exception:
        terminate_pid(backend_proc.pid)
        raise

    n8n_proc: subprocess.Popen | None = None
    if with_n8n and not (n8n_pid and is_running(n8n_pid)):
        n8n_cmd = choose_n8n_command(args)
        try:
            n8n_proc = subprocess.Popen(
                n8n_cmd,
                cwd=REPO_ROOT,
                env=env,
                creationflags=creationflags,
            )
        except Exception:
            # n8n is optional for service launch; keep backend/frontend alive.
            n8n_proc = None

    frontend_mode = "dev-vite"
    frontend_fallback_note = ""
    backend_ok, backend_msg = _wait_for_tcp_ready(
        "backend",
        host=args.host,
        port=int(args.backend_port),
        timeout_seconds=20,
        proc=backend_proc,
    )
    frontend_ok, frontend_msg = _wait_for_tcp_ready(
        "frontend",
        host=args.host,
        port=int(args.frontend_port),
        timeout_seconds=25,
        proc=frontend_proc,
    )
    if not frontend_ok:
        terminate_pid(frontend_proc.pid)
        fallback_proc, fallback_err = _start_frontend_static_server(args, env, creationflags)
        if fallback_proc is not None:
            frontend_proc = fallback_proc
            frontend_mode = "prod-static"
            frontend_fallback_note = "Frontend dev failed; using static bundle fallback."
            frontend_ok, frontend_msg = _wait_for_tcp_ready(
                "frontend-static",
                host=args.host,
                port=int(args.frontend_port),
                timeout_seconds=20,
                proc=frontend_proc,
            )
        elif fallback_err:
            frontend_msg = f"{frontend_msg} {fallback_err}".strip()
    if not backend_ok or not frontend_ok:
        terminate_pid(frontend_proc.pid)
        terminate_pid(backend_proc.pid)
        if n8n_proc is not None:
            terminate_pid(n8n_proc.pid)
        failures = [msg for msg in (backend_msg, frontend_msg) if msg]
        detail = " ".join(failures).strip() or "Startup health checks failed."
        raise SystemExit(f"Service startup failed. {detail}")

    # Guard against false positives when requested ports are occupied by other
    # processes and child processes exit immediately after spawn.
    time.sleep(1.0)
    exited_msgs: list[str] = []
    if backend_proc.poll() is not None:
        exited_msgs.append(f"backend exited early (code {backend_proc.returncode})")
    if frontend_proc.poll() is not None:
        exited_msgs.append(f"frontend exited early (code {frontend_proc.returncode})")
    if exited_msgs:
        terminate_pid(frontend_proc.pid)
        terminate_pid(backend_proc.pid)
        if n8n_proc is not None:
            terminate_pid(n8n_proc.pid)
        hint = "requested ports may already be in use."
        raise SystemExit(f"Service startup failed. {'; '.join(exited_msgs)} ({hint})")

    data = {
        "backend_pid": backend_proc.pid,
        "frontend_pid": frontend_proc.pid,
        "n8n_pid": int(n8n_proc.pid) if n8n_proc else (n8n_pid if n8n_pid and is_running(n8n_pid) else 0),
        "backend_port": args.backend_port,
        "frontend_port": args.frontend_port,
        "n8n_port": args.n8n_port,
        "frontend_mode": frontend_mode,
        "started_at": time.time(),
    }
    save_pids(data)
    print(f"Backend started (PID {backend_proc.pid}) on http://localhost:{args.backend_port}")
    print(f"Frontend started (PID {frontend_proc.pid}) on http://localhost:{args.frontend_port} [{frontend_mode}]")
    if frontend_fallback_note:
        print(frontend_fallback_note)
    if n8n_proc:
        print(f"n8n started (PID {n8n_proc.pid}) on http://localhost:{args.n8n_port}")
    elif with_n8n:
        print(f"n8n requested. If not running, open Automation tab to install/start local n8n on port {args.n8n_port}.")
    if bool(getattr(args, "open", False)):
        open_ui(args)


def stop_services(_: argparse.Namespace) -> None:
    pids = load_pids()
    stopped_any = False
    for key in ("frontend_pid", "backend_pid", "desktop_pid", "n8n_pid"):
        pid = int(pids.get(key, 0) or 0)
        if pid and terminate_pid(pid):
            print(f"Stopped {key} ({pid}).")
            stopped_any = True
    if not stopped_any:
        print("No recorded Agent Space processes were running.")
    if PID_FILE.exists():
        PID_FILE.unlink()


def open_ui(args: argparse.Namespace) -> None:
    url = f"http://localhost:{args.frontend_port}"
    _open_url(url)
    print(f"Opened {url}")


def start_desktop(args: argparse.Namespace) -> None:
    if args.with_services:
        start_services(args)

    if not DESKTOP_MAIN.exists():
        raise SystemExit(f"Desktop entry not found: {DESKTOP_MAIN}")

    env = os.environ.copy()
    env["AGENTSPACE_UI_URL"] = f"http://localhost:{args.frontend_port}"
    electron_bin = choose_electron()
    if " " in electron_bin and electron_bin.endswith("cli.js"):
        # Fallback path for non-Windows environments with JS entrypoint.
        electron_cmd = electron_bin.split(" ", 1) + [str(DESKTOP_MAIN)]
    else:
        electron_cmd = [electron_bin, str(DESKTOP_MAIN)]
    creationflags = 0
    if os.name == "nt":
        creationflags = subprocess.CREATE_NEW_PROCESS_GROUP  # type: ignore[attr-defined]
    proc = subprocess.Popen(electron_cmd, cwd=REPO_ROOT, env=env, creationflags=creationflags)
    time.sleep(0.8)
    if proc.poll() is not None:
        raise SystemExit(f"Desktop app failed to start (exit code {proc.returncode}).")

    pids = load_pids()
    pids["desktop_pid"] = proc.pid
    pids["frontend_port"] = args.frontend_port
    pids["backend_port"] = args.backend_port
    save_pids(pids)
    print(f"Desktop app started (PID {proc.pid}).")


def create_shortcuts(_: argparse.Namespace) -> None:
    desktop = Path.home() / "Desktop"
    output_dir = desktop
    try:
        output_dir.mkdir(parents=True, exist_ok=True)
    except Exception:
        output_dir = REPO_ROOT / "data" / "agent_space" / "runtime" / "desktop_shortcuts"
        output_dir.mkdir(parents=True, exist_ok=True)
    commands = {
        "Agent Space - Start.cmd": _lifecycle_cmd("start --open"),
        "Agent Space - Start + n8n.cmd": _lifecycle_cmd("start --open --with-n8n"),
        "Agent Space - Stop.cmd": _lifecycle_cmd("stop"),
        "Agent Space - Open UI.cmd": _lifecycle_cmd("open-ui"),
        "Agent Space - Desktop.cmd": _lifecycle_cmd("desktop --with-services"),
        "Agent Space - Shortcuts.cmd": _lifecycle_cmd("shortcuts"),
        "jimAI - Free Stack Setup.cmd": _lifecycle_cmd("free-stack-setup --start --open --shortcuts"),
        "jimAI - Install Podman Runtime.cmd": _lifecycle_cmd("free-stack-install-runtime --runtime podman"),
        "jimAI - Install Docker Runtime.cmd": _lifecycle_cmd("free-stack-install-runtime --runtime docker"),
        "jimAI - Free Stack Start.cmd": _lifecycle_cmd("free-stack-start --open"),
        "jimAI - Free Stack Stop.cmd": _lifecycle_cmd("free-stack-stop"),
        "jimAI - Free Stack Open Dashboards.cmd": _lifecycle_cmd("free-stack-open"),
    }
    for filename, command in commands.items():
        content = f"@echo off\r\ncd /d \"{REPO_ROOT}\"\r\n{command}\r\n"
        try:
            (output_dir / filename).write_text(content, encoding="utf-8")
        except PermissionError:
            fallback = REPO_ROOT / "data" / "agent_space" / "runtime" / "desktop_shortcuts"
            fallback.mkdir(parents=True, exist_ok=True)
            (fallback / filename).write_text(content, encoding="utf-8")
            output_dir = fallback
    print(f"Created {len(commands)} desktop shortcuts in {output_dir}")


def free_stack_setup(args: argparse.Namespace) -> None:
    if not FREE_STACK_COMPOSE.exists():
        raise SystemExit(f"Free stack compose file not found: {FREE_STACK_COMPOSE}")

    env_map = _ensure_free_stack_env(force_rotate_passwords=bool(getattr(args, "rotate_passwords", False)))
    if bool(getattr(args, "shortcuts", False)):
        _create_free_stack_shortcuts(env_map)

    should_pull = bool(getattr(args, "pull", True))
    should_start = bool(getattr(args, "start", False))
    if should_pull or should_start:
        compose = _detect_compose_base()
        print(f"Using compose runtime: {' '.join(compose)}")

        if should_pull:
            pull = _run_compose(compose, ["pull"], env_file=FREE_STACK_ENV_FILE)
            if pull.returncode != 0:
                raise SystemExit("Failed to download free stack images. Check container runtime and internet access.")

        if should_start:
            up = _run_compose(compose, ["up", "-d"], env_file=FREE_STACK_ENV_FILE)
            if up.returncode != 0:
                raise SystemExit("Failed to start free stack containers.")

    _print_free_stack_credential_summary(env_map)
    if bool(getattr(args, "open", False)):
        _open_free_stack_urls(env_map)


def free_stack_start(args: argparse.Namespace) -> None:
    env_map = _ensure_free_stack_env(force_rotate_passwords=False)
    compose = _detect_compose_base()
    up = _run_compose(compose, ["up", "-d"], env_file=FREE_STACK_ENV_FILE)
    if up.returncode != 0:
        raise SystemExit("Failed to start free stack containers.")
    print("Free stack containers started.")
    _print_free_stack_credential_summary(env_map)
    if bool(getattr(args, "open", False)):
        _open_free_stack_urls(env_map)


def free_stack_stop(_: argparse.Namespace) -> None:
    compose = _detect_compose_base()
    down = _run_compose(compose, ["down"], env_file=FREE_STACK_ENV_FILE)
    if down.returncode != 0:
        raise SystemExit("Failed to stop free stack containers.")
    print("Free stack containers stopped.")


def free_stack_open(_: argparse.Namespace) -> None:
    env_map = _ensure_free_stack_env(force_rotate_passwords=False)
    _open_free_stack_urls(env_map)
    _print_free_stack_credential_summary(env_map)


def free_stack_status(_: argparse.Namespace) -> None:
    compose = _detect_compose_base()
    ps = _run_compose(compose, ["ps"], env_file=FREE_STACK_ENV_FILE)
    if ps.returncode != 0:
        raise SystemExit("Failed to read free stack status.")


def free_stack_install_runtime(args: argparse.Namespace) -> None:
    runtime = str(getattr(args, "runtime", "podman")).strip().lower()
    winget = shutil.which("winget")
    if not winget:
        raise SystemExit(
            "winget is not available on this machine. Install Docker/Podman manually and rerun free-stack setup."
        )

    package_id = "RedHat.Podman-Desktop" if runtime == "podman" else "Docker.DockerDesktop"
    cmd = [
        winget,
        "install",
        "--id",
        package_id,
        "-e",
        "--accept-package-agreements",
        "--accept-source-agreements",
    ]
    print(f"Installing runtime via winget: {package_id}")
    result = subprocess.run(cmd, cwd=REPO_ROOT, text=True, check=False)
    if result.returncode != 0:
        raise SystemExit(f"Runtime install command failed for {package_id}.")
    print(f"Runtime install command completed for {package_id}.")


