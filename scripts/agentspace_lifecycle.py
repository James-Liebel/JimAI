def _random_secret(length: int = 26) -> str:
    raw = secrets.token_urlsafe(max(20, length))
    return raw[:length]


def _parse_env_file(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    data: dict[str, str] = {}
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        data[key.strip()] = value.strip()
    return data


def _write_env_file(path: Path, env_map: dict[str, str]) -> None:
    lines = ["# Auto-generated by scripts/agentspace_lifecycle.py"]
    for key in sorted(env_map.keys()):
        lines.append(f"{key}={env_map[key]}")
    ensure_secure_dir()
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    _restrict_file_permissions(path)


