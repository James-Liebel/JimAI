def choose_tool(name: str) -> str:
    if os.name == "nt":
        local_bin = REPO_ROOT / "node_modules" / ".bin" / f"{name}.cmd"
        if local_bin.exists():
            return str(local_bin)
    if os.name == "nt":
        win = shutil.which(f"{name}.cmd")
        if win:
            return win
    resolved = shutil.which(name)
    if resolved:
        return resolved
    raise SystemExit(
        f"Required tool '{name}' was not found in PATH. "
        f"Install Node.js/npm and reopen the launcher."
    )


def _build_frontend_bundle(env: dict[str, str]) -> tuple[bool, str]:
    cmd = [choose_tool("npm"), "run", "build"]
    proc = subprocess.run(
        cmd,
        cwd=FRONTEND_DIR,
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        check=False,
    )
    if proc.returncode == 0:
        return True, ""
    tail = "\n".join((proc.stdout or "").splitlines()[-8:] + (proc.stderr or "").splitlines()[-8:]).strip()
    reason = tail if tail else f"npm build exited with code {proc.returncode}"
    return False, reason


def _start_frontend_static_server(
    args: argparse.Namespace,
    env: dict[str, str],
    creationflags: int,
    *,
    rebuild: bool = False,
) -> tuple[subprocess.Popen | None, str]:
    dist_dir = FRONTEND_DIR / "dist"
    if rebuild or not dist_dir.exists():
        ok, reason = _build_frontend_bundle(env)
        if not ok:
            return None, f"static fallback build failed: {reason}"
    cmd = [
        choose_backend_python(),
        str(FRONTEND_STATIC_SCRIPT),
        "--host",
        args.host,
        "--port",
        str(args.frontend_port),
    ]
    try:
        proc = subprocess.Popen(
            cmd,
            cwd=REPO_ROOT,
            env=env,
            creationflags=creationflags,
        )
    except Exception as exc:
        return None, f"static fallback launch failed: {exc}"
    return proc, ""


