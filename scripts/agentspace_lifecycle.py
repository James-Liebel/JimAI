def _detect_compose_base() -> list[str]:
    candidates: list[list[str]] = [
        ["docker", "compose"],
        ["docker-compose"],
        ["podman", "compose"],
        ["podman-compose"],
    ]
    for base in candidates:
        try:
            probe = subprocess.run(
                [*base, "version"],
                cwd=REPO_ROOT,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
                timeout=20,
                check=False,
            )
        except (OSError, FileNotFoundError, subprocess.TimeoutExpired):
            continue
        if probe.returncode == 0:
            return base
    raise SystemExit(
        "No supported container compose runtime found.\n"
        "Install one of: Docker (with compose plugin), docker-compose, Podman (compose), or podman-compose.\n"
        "Windows examples:\n"
        "  winget install Docker.DockerDesktop\n"
        "  winget install RedHat.Podman-Desktop"
    )


def _run_compose(base: list[str], compose_args: list[str], *, env_file: Path) -> subprocess.CompletedProcess:
    cmd = [*base, "--env-file", str(env_file), "-f", str(FREE_STACK_COMPOSE), "-p", FREE_STACK_PROJECT, *compose_args]
    compose_env = os.environ.copy()
    compose_env.update(_parse_env_file(env_file))
    return subprocess.run(cmd, cwd=REPO_ROOT, env=compose_env, text=True, check=False)


