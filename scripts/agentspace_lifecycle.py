def main() -> None:
    parser = build_parser()
    args = parser.parse_args()
    ensure_runtime_dir()

    if args.command == "start":
        start_services(args)
    elif args.command == "stop":
        stop_services(args)
    elif args.command == "open-ui":
        open_ui(args)
    elif args.command == "desktop":
        start_desktop(args)
    elif args.command == "shortcuts":
        create_shortcuts(args)
    elif args.command == "free-stack-setup":
        free_stack_setup(args)
    elif args.command == "free-stack-start":
        free_stack_start(args)
    elif args.command == "free-stack-stop":
        free_stack_stop(args)
    elif args.command == "free-stack-open":
        free_stack_open(args)
    elif args.command == "free-stack-status":
        free_stack_status(args)
    elif args.command == "free-stack-install-runtime":
        free_stack_install_runtime(args)
    else:
        parser.error(f"Unknown command: {args.command}")


if __name__ == "__main__":
    main()
